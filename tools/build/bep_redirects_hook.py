"""MkDocs hook: auto-populate ``/bepXXX`` redirects from data/beps/beps.yml.

The mkdocs-redirects plugin normally reads its ``redirect_maps`` from
``mkdocs.yml``. For every BEP whose page is generated by
``tools/build/generate_bep_pages.py`` (i.e. every entry in
``data/beps/beps.yml``), this hook adds a matching
``bep{NUMBER}.md`` -> ``extensions/beps/bep_{NUMBER}.md`` entry, so new
BEPs get their shortcut URL automatically.

The target is an internal doc path (not a full URL), so mkdocs-redirects
emits a relative link. That means the shortcut works on any host the
site is served from - production (``bids.neuroimaging.io``), Netlify PR
previews, ``mkdocs serve`` at ``localhost:8000``, or offline previews of
the built ``site/`` directory.

Manual entries in ``mkdocs.yml`` always win: this hook only adds keys
that are not already present. That preserves the hand-curated redirects
for BEPs whose content was merged into the specification (BEP 001-010,
018, 022, 027) and point to external URLs.
"""

from pathlib import Path

import ruamel.yaml

_ROOT = Path(__file__).resolve().parents[2]
_BEPS_YML = _ROOT / "data" / "beps" / "beps.yml"
_TARGET_TEMPLATE = "extensions/beps/bep_{number}.md"


def _load_bep_numbers() -> list[str]:
    yaml = ruamel.yaml.YAML(typ="safe")
    beps = yaml.load(_BEPS_YML) or []
    return [bep["number"] for bep in beps if bep.get("number")]


def on_config(config):
    redirects = config["plugins"].get("redirects")
    if redirects is None:
        return config
    redirect_maps = redirects.config["redirect_maps"]
    for number in _load_bep_numbers():
        key = f"bep{number}.md"
        if key in redirect_maps:
            continue
        redirect_maps[key] = _TARGET_TEMPLATE.format(number=number)
    return config
