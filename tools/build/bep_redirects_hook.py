"""MkDocs hook: auto-populate ``/bepXXX`` redirects from data/beps/beps.yml.

Two things happen in ``on_config``:

1. For every BEP whose page is generated by
   ``tools/build/generate_bep_pages.py`` (i.e. every entry in
   ``data/beps/beps.yml``), a ``bep{NUMBER}.md`` redirect is added
   pointing at the internal doc path ``extensions/beps/bep_{NUMBER}.md``.
   The mkdocs-redirects plugin turns that into a *relative* link in
   the generated redirect HTML, so the shortcut works on any host the
   site is served from - production, Netlify PR previews,
   ``mkdocs serve`` on localhost, offline previews of ``site/``.

2. For every ``bep{NUMBER}.md`` entry in ``redirect_maps`` (both the
   ones we just added and the manually curated entries in
   ``mkdocs.yml``), a mirrored ``bep{NUMBER}/index.md`` entry is added
   with the same target. The mkdocs-redirects plugin then produces
   *both* ``site/bepNNN.html`` and ``site/bepNNN/index.html``.
   That way ``mkdocs serve`` (whose dev server does no ``.html``
   fallback) serves ``/bepNNN`` as well as ``/bepNNN.html``. On
   GitHub Pages the ``.html`` fallback already handled both, so this
   only changes behavior on the local dev server and other strict
   static hosts.

Manual entries in ``mkdocs.yml`` always win: this hook only adds keys
that are not already present.
"""

import re
from pathlib import Path

import ruamel.yaml

_ROOT = Path(__file__).resolve().parents[2]
_BEPS_YML = _ROOT / "data" / "beps" / "beps.yml"
_TARGET_TEMPLATE = "extensions/beps/bep_{number}.md"
_BEP_KEY_RE = re.compile(r"(bep\d+)\.md")


def _load_bep_numbers() -> list[str]:
    yaml = ruamel.yaml.YAML(typ="safe")
    beps = yaml.load(_BEPS_YML) or []
    return [bep["number"] for bep in beps if bep.get("number")]


def on_config(config):
    redirects = config["plugins"].get("redirects")
    if redirects is None:
        return config
    redirect_maps = redirects.config["redirect_maps"]

    for number in _load_bep_numbers():
        key = f"bep{number}.md"
        if key not in redirect_maps:
            redirect_maps[key] = _TARGET_TEMPLATE.format(number=number)

    for key in list(redirect_maps):
        m = _BEP_KEY_RE.fullmatch(key)
        if not m:
            continue
        mirror = f"{m.group(1)}/index.md"
        if mirror not in redirect_maps:
            redirect_maps[mirror] = redirect_maps[key]

    return config
